function y=MCableElementAssemble(K,k,i,j)
%k表示索单元单元刚度矩阵，为4*4矩阵；
%K表示将要代入的总体刚度矩阵；
%i表示索单元左节点号(总体刚度矩阵中号）；
%j表示索单元右节点号(总体刚度矩阵中号）；
K(3*i-2,3*i-2)=K(3*i-2,3*i-2)+k(1,1);
K(3*i-2,3*i-1)=K(3*i-2,3*i-1)+k(1,2);
K(3*i-2,3*j-1)=K(3*i-2,3*j-1)+k(1,3);
K(3*i-2,3*j-2)=K(3*i-2,3*j-2)+k(1,4);

K(3*i-1,3*i-2)=K(3*i-1,3*i-2)+k(2,1);
K(3*i-1,3*i-1)=K(3*i-1,3*i-1)+k(2,2);
K(3*i-1,3*j-1)=K(3*i-1,3*j-1)+k(2,3);
K(3*i-1,3*j-2)=K(3*i-1,3*j-2)+k(2,4);

K(3*j-1,3*i-2)=K(3*j-1,3*i-2)+k(3,1);
K(3*j-1,3*i-1)=K(3*j-1,3*i-1)+k(3,2);
K(3*j-1,3*j-1)=K(3*j-1,3*j-1)+k(3,3);
K(3*j-1,3*j-2)=K(3*j-1,3*j-2)+k(3,4);

K(3*j-2,3*i-2)=K(3*j-2,3*i-2)+k(4,1);
K(3*j-2,3*i-1)=K(3*j-2,3*i-1)+k(4,2);
K(3*j-2,3*j-1)=K(3*j-2,3*j-1)+k(4,3);
K(3*j-2,3*j-2)=K(3*j-2,3*j-2)+k(4,4);

y=K;
end

